import { FETCH_INGREDIENTS_REQUEST, FETCH_INGREDIENTS_SUCCESS, FETCH_INGREDIENTS_FAILURE } from "../actions/ingredients-actions";

const initialState = {
    ingredients: [],
    loading: true,
    error: null
};

const ingredientsReducer = (state = initialState, action) => {
    switch (action.type) {
        case FETCH_INGREDIENTS_REQUEST:
            return {
                ...state,
            };
        case FETCH_INGREDIENTS_SUCCESS:
            return {
                ...state,
                loading: false,
                ingredients: action.payload
            };
        case FETCH_INGREDIENTS_FAILURE:
            return {
                ...state,
                loading: false,
                error: action.payload
            };
        default:
            return state;
    }
};

export default ingredientsReducer;
