import { SET_CURRENT_INGREDIENT, CLEAR_CURRENT_INGREDIENT } from "../actions/current-ingredient-actions";

const initialState = {
    ingredient: null
};

const currentIngredientReducer = (state = initialState, action) => {
    switch (action.type) {
        case SET_CURRENT_INGREDIENT:
            return {
                ...state,
                ingredient: action.payload
            };
        case CLEAR_CURRENT_INGREDIENT:
            return initialState;
        default:
            return state;
    }
};

export default currentIngredientReducer;