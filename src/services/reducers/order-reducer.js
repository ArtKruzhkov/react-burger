import { SET_ORDER, CLEAR_ORDER } from "../actions/order-actions";

const initialState = {
    orderNumber: null,
    orderName: ''
};

const orderReducer = (state = initialState, action) => {
    switch (action.type) {
        case SET_ORDER:
            return {
                ...state,
                orderNumber: action.payload.orderNumber,
                orderName: action.payload.orderName
            };
        case CLEAR_ORDER:
            return initialState;
        default:
            return state;
    }
};

export default orderReducer;